#!/usr/bin/env node
/**
 * Build script for GitHub Pages deployment.
 *
 * Transforms the frontend/ directory into a GitHub Pages-compatible
 * build that works under the /taronyang/ base path.
 *
 * Key transformations:
 *   - Creates static/css/ and static/js/ from css/ and js/
 *   - Injects a fetch interceptor that redirects /api/* to the Quick Tunnel URL
 *   - Rewrites all absolute paths (href, src, location.href) to /taronyang/*
 *   - Creates directory-based routing for clean URLs (e.g. /tarot → tarot/index.html)
 *   - Adds .nojekyll to bypass Jekyll processing
 *
 * Usage:
 *   TUNNEL_URL=https://xxx.trycloudflare.com node scripts/build-gh-pages.mjs
 */

import { cpSync, mkdirSync, writeFileSync, readFileSync, existsSync, rmSync, readdirSync, statSync } from 'fs';
import { join, dirname, relative, extname } from 'path';
import { fileURLToPath } from 'url';
import { generateCardPages } from './generate-card-pages.mjs';
import { ALL_CARDS } from './card-data.mjs';

const __scriptDir = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__scriptDir, '..');
const FRONTEND = join(ROOT, 'frontend');
const BUILD = join(ROOT, 'gh-pages-build');
const BASE_PATH = '/taronyang';
const TUNNEL_URL = process.env.TUNNEL_URL || '';

// SEO / Analytics injection (ZEMA-2794) — env-var based, optional.
// Values provided at deploy time by the board (Google accounts).
// When absent, build output is unchanged (graceful no-op).
// Trimmed + format-validated so typos fail fast at build time.
const GSC_VERIFICATION_CODE = (process.env.GSC_VERIFICATION_CODE || '').trim();
const GA4_MEASUREMENT_ID = (process.env.GA4_MEASUREMENT_ID || '').trim();

if (GSC_VERIFICATION_CODE && !/^[a-zA-Z0-9_-]+$/.test(GSC_VERIFICATION_CODE)) {
  throw new Error(`Invalid GSC_VERIFICATION_CODE format: "${GSC_VERIFICATION_CODE}"`);
}
if (GA4_MEASUREMENT_ID && !/^[A-Za-z0-9-]+$/.test(GA4_MEASUREMENT_ID)) {
  throw new Error(`Invalid GA4_MEASUREMENT_ID format: "${GA4_MEASUREMENT_ID}"`);
}

function minifyCss(css) {
  return css
    .replace(/\/\*[\s\S]*?\*\//g, '')
    .replace(/\s+/g, ' ')
    .replace(/\s*([{}:;,>])\s*/g, '$1')
    .replace(/;}/g, '}')
    .replace(/:\s+/g, ':')
    .trim();
}

// Domain currently embedded in source files → target deployment URL.
// Source files now embed the GitHub Pages URL (ZEMA-2747), so the search
// domain must match what is actually present for the rewrite to work.
// To roll back to a custom domain, set SITE_URL=https://taronyang.com and
// the build will replace the GitHub Pages URL with the custom domain.
const CANONICAL_DOMAIN = (process.env.CANONICAL_DOMAIN || 'https://zemyalpha.github.io/taronyang').replace(/\/$/, '');
const GH_PAGES_URL = (process.env.SITE_URL || 'https://zemyalpha.github.io/taronyang').replace(/\/$/, '');

// File extensions that may contain taronyang.com URLs and need domain rewriting
const DOMAIN_REWRITE_EXTENSIONS = new Set(['.html', '.xml', '.txt', '.js', '.json']);

// Pages that need directory-based routing (clean URL → dir/index.html)
const ROUTED_PAGES = ['tarot', 'daily', 'history', 'mypage', 'login', 'pricing'];

function findHtmlFiles(dir) {
  const results = [];
  for (const entry of readdirSync(dir)) {
    const fullPath = join(dir, entry);
    const stat = statSync(fullPath);
    if (stat.isDirectory()) {
      results.push(...findHtmlFiles(fullPath));
    } else if (extname(entry) === '.html') {
      results.push(fullPath);
    }
  }
  return results;
}

function generateConfigJs(tunnelUrl) {
  // 빌드 시점 URL — 폴백으로만 사용 (런타임 비컨이 우선)
  var buildApiBase = tunnelUrl ? `${tunnelUrl.replace(/\/$/, '')}/api` : '';

  return [
    '// Auto-generated by build-gh-pages.mjs — runtime API discovery (ZEMA-2620)',
    '// 아키텍처: 비컨 fetch → localStorage 캐시 → 빌드 URL → same-origin 폴백',
    'window.__TARONYANG_CONFIG__ = window.__TARONYANG_CONFIG__ || {};',
    '',
    '(function () {',
    '  var BUILD_API_BASE = ' + JSON.stringify(buildApiBase) + ';',
    '  var CACHE_KEY = "taronyang_api_base";',
    '  var CACHE_TTL = 1000 * 60 * 60 * 24; // 24시간',
    '  var basePath = ' + JSON.stringify(BASE_PATH) + ';',
    '',
    '  // 런타임에 비동기로 API URL 발견 — config.js 로드 즉시 실행',
    '  window.__TARONYANG_API_READY__ = (async function () {',
    '    var discovered = null;',
    '',
    '    // 1단계: 동일 출처 비컨 파일에서 현재 API URL 가져오기',
    '    try {',
    '      var beaconBase = window.location.origin + (basePath ? basePath : "") + "/api-beacon.json";',
    '      var resp = await fetch(beaconBase, { cache: "no-cache" });',
    '      if (resp.ok) {',
    '        var data = await resp.json();',
    '        if (data && data.apiUrl) {',
    '          discovered = data.apiUrl.replace(/\\/$/, "") + "/api";',
    '        }',
    '      }',
    '    } catch (e) { /* 비컨 파일 없음 — 다음 단계로 */ }',
    '',
    '    // 2단계: localStorage 캐시 확인',
    '    if (!discovered) {',
    '      try {',
    '        var cached = JSON.parse(localStorage.getItem(CACHE_KEY) || "{}");',
    '        if (cached.apiBase && (Date.now() - (cached.ts || 0) < CACHE_TTL)) {',
    '          discovered = cached.apiBase;',
    '        }',
    '      } catch (e) { /* localStorage 접근 불가 — 무시 */ }',
    '    }',
    '',
    '    // 3단계: 빌드 시점 URL 폴백',
    '    if (!discovered && BUILD_API_BASE) {',
    '      discovered = BUILD_API_BASE;',
    '    }',
    '',
    '    // 발견된 API URL 설정 + 캐시 저장',
    '    if (discovered) {',
    '      window.__TARONYANG_CONFIG__.apiBase = discovered;',
    '      try {',
    '        localStorage.setItem(CACHE_KEY, JSON.stringify({ apiBase: discovered, ts: Date.now() }));',
    '      } catch (e) { /* localStorage 접근 불가 — 무시 */ }',
    '    }',
    '',
    '    return discovered;',
    '  })();',
    '',
    '  // fetch 인터셉터 — API 호출을 발견된 URL로 리다이렉트',
    '  // 첫 API 호출 전에 런타임 발견이 완료되도록 보장',
    '  var origFetch = window.fetch.bind(window);',
    '  window.fetch = function (input, init) {',
    '    var url = typeof input === "string" ? input : (input && input.url) || "";',
    '    var path = url.replace(window.location.origin, "");',
    '    if (/^\\/api([\\/?]|$)/.test(path)) {',
    '      var rest = path.replace(/^\\/api/, "");',
    '      // 런타임 발견 완료 대기 후 URL 리다이렉트',
    '      return window.__TARONYANG_API_READY__.then(function (discovered) {',
    '        var apiBase = discovered || BUILD_API_BASE || (window.location.origin + "/api");',
    '        var newUrl = apiBase + rest;',
    '        if (typeof input === "string") return origFetch(newUrl, init);',
    '        // Request 객체: 속성을 plain RequestInit로 복사 (spec-compliant)',
    '        var ri = { method: input.method, headers: input.headers, mode: input.mode,',
    '          credentials: input.credentials, cache: input.cache, redirect: input.redirect,',
    '          signal: input.signal };',
    '        if (input.method !== "GET" && input.method !== "HEAD") { ri.body = input.body; ri.duplex = "half"; }',
    '        return origFetch(newUrl, Object.assign(ri, init || {}));',
    '      });',
    '    }',
    '    return origFetch(input, init);',
    '  };',
    '})();',
    '',
  ].join('\n');
}

function injectConfigScript(html) {
  const scriptTag = `<script src="${BASE_PATH}/static/js/config.js"></script>`;
  if (html.includes('static/js/config.js')) return html;
  return html.replace(/<head\b[^>]*>/i, (match) => `${match}\n    ${scriptTag}`);
}

/**
 * Inject GSC verification meta tag and/or GA4 gtag snippet into <head>.
 *
 * Both are env-var gated (ZEMA-2794):
 *   - GSC_VERIFICATION_CODE → <meta name="google-site-verification" content="...">
 *   - GA4_MEASUREMENT_ID    → Google gtag.js snippet
 *
 * Idempotent: skips injection if the tag/snippet is already present.
 * When neither env var is set, the HTML is returned unchanged.
 */
function injectSeoTags(html) {
  if (!GSC_VERIFICATION_CODE && !GA4_MEASUREMENT_ID) return html;

  const injections = [];

  if (GSC_VERIFICATION_CODE && !html.includes('google-site-verification')) {
    const safeGsc = GSC_VERIFICATION_CODE.replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/</g, '&lt;');
    injections.push(
      `<meta name="google-site-verification" content="${safeGsc}">`,
    );
  }

  if (GA4_MEASUREMENT_ID && !html.includes('googletagmanager.com/gtag/js')) {
    const safeGa4Url = encodeURIComponent(GA4_MEASUREMENT_ID);
    const safeGa4Js = GA4_MEASUREMENT_ID.replace(/'/g, "\\'");
    injections.push(
      `<script async src="https://www.googletagmanager.com/gtag/js?id=${safeGa4Url}"></script>`,
      `<script>window.dataLayer=window.dataLayer||[];function gtag(){dataLayer.push(arguments);}gtag('js',new Date());gtag('config','${safeGa4Js}');</script>`,
    );
  }

  if (injections.length === 0) return html;

  const block = injections.map((tag) => `    ${tag}`).join('\n') + '\n';
  return html.replace(/<\/head>/i, (match) => `${block}${match}`);
}

function rewriteDomain(content) {
  return content.split(CANONICAL_DOMAIN).join(GH_PAGES_URL);
}

function rewritePaths(html) {
  let result = html;

  // Replace canonical domain → GitHub Pages URL (OG, canonical, JSON-LD, sitemap, etc.)
  result = rewriteDomain(result);

  // href/src/action="/..." → "/taronyang/..."  (but not if already prefixed or protocol-relative //)
  result = result.replace(
    /((?:href|src|action)\s*=\s*["'])\/(?![/]|taronyang\/)/g,
    `$1${BASE_PATH}/`,
  );

  // location.href = '/...', location = '/...', location.assign('/...'), location.replace('/...')
  // Also catches onclick="location.href='/...'"
  result = result.replace(
    /((?:window\.)?location(?:\.href|\.assign|\.replace)?\s*(?:=\s*|\(\s*)["'])\/(?![/]|taronyang\/)/g,
    `$1${BASE_PATH}/`,
  );

  return result;
}

function updateSitemapWithCards(buildDir) {
  const sitemapPath = join(buildDir, 'sitemap.xml');
  if (!existsSync(sitemapPath)) return;
  let sitemap = readFileSync(sitemapPath, 'utf-8');

  // Remove any previously injected card entries
  sitemap = sitemap.replace(/\n  <!-- card-pages-start -->[\s\S]*?<!-- card-pages-end -->/g, '');

  const today = new Date().toISOString().split('T')[0];
  const cardUrls = ALL_CARDS.map((card) => {
    const slug = `${card.id}-${card.slug}`;
    return [
      `  <url>`,
      `    <loc>${GH_PAGES_URL}/cards/${slug}.html</loc>`,
      `    <lastmod>${today}</lastmod>`,
      `    <changefreq>monthly</changefreq>`,
      `    <priority>0.7</priority>`,
      `  </url>`,
    ].join('\n');
  }).join('\n');

  const cardsIndexUrl = [
    `  <url>`,
    `    <loc>${GH_PAGES_URL}/cards/</loc>`,
    `    <lastmod>${today}</lastmod>`,
    `    <changefreq>weekly</changefreq>`,
    `    <priority>0.8</priority>`,
    `  </url>`,
  ].join('\n');

  const injection = `\n  <!-- card-pages-start -->\n${cardsIndexUrl}\n${cardUrls}\n  <!-- card-pages-end -->`;
  sitemap = sitemap.replace('</urlset>', `${injection}\n</urlset>`);
  writeFileSync(sitemapPath, sitemap);
  console.log(`  ✓ Added ${ALL_CARDS.length} card URLs to sitemap.xml`);
}

function updateSitemapWithDailyFortunes(buildDir) {
  const sitemapPath = join(buildDir, 'sitemap.xml');
  if (!existsSync(sitemapPath)) return;

  const dailyDir = join(buildDir, 'blog', 'daily');
  if (!existsSync(dailyDir)) return;

  const fortunes = readdirSync(dailyDir)
    .filter((f) => /^\d{4}-\d{2}-\d{2}\.html$/.test(f))
    .map((f) => f.replace('.html', ''))
    .sort()
    .reverse();

  if (fortunes.length === 0) return;

  let sitemap = readFileSync(sitemapPath, 'utf-8');
  sitemap = sitemap.replace(/\n  <!-- daily-fortune-start -->[\s\S]*?<!-- daily-fortune-end -->/g, '');

  const today = new Date().toISOString().split('T')[0];

  const indexUrl = [
    `  <url>`,
    `    <loc>${GH_PAGES_URL}/blog/daily/</loc>`,
    `    <lastmod>${today}</lastmod>`,
    `    <changefreq>daily</changefreq>`,
    `    <priority>0.9</priority>`,
    `  </url>`,
  ].join('\n');

  const fortuneUrls = fortunes.map((dateStr) => [
    `  <url>`,
    `    <loc>${GH_PAGES_URL}/blog/daily/${dateStr}.html</loc>`,
    `    <lastmod>${dateStr}</lastmod>`,
    `    <changefreq>monthly</changefreq>`,
    `    <priority>0.8</priority>`,
    `  </url>`,
  ].join('\n')).join('\n');

  const injection = `\n  <!-- daily-fortune-start -->\n${indexUrl}\n${fortuneUrls}\n  <!-- daily-fortune-end -->`;
  sitemap = sitemap.replace('</urlset>', `${injection}\n</urlset>`);
  writeFileSync(sitemapPath, sitemap);
  console.log(`  ✓ Added ${fortunes.length} daily fortune URLs to sitemap.xml`);
}

// === Build ===
console.log('[build] Cleaning build directory...');
rmSync(BUILD, { recursive: true, force: true });
mkdirSync(BUILD, { recursive: true });

// 2. Copy frontend files (excluding functions/ and wrangler.toml which are Cloudflare-specific)
console.log('[build] Copying frontend files...');
const skipEntries = new Set(['functions', 'wrangler.toml', 'node_modules', '.DS_Store']);
for (const entry of readdirSync(FRONTEND)) {
  if (skipEntries.has(entry)) continue;
  cpSync(join(FRONTEND, entry), join(BUILD, entry), { recursive: true });
}

// 3. Create static/ directory structure (backend maps /static → frontend root)
console.log('[build] Creating static/ directory mapping...');
mkdirSync(join(BUILD, 'static'), { recursive: true });
for (const entry of readdirSync(BUILD)) {
  if (entry === 'static' || entry.endsWith('.html') || entry.startsWith('.')) continue;
  const fullPath = join(BUILD, entry);
  if (statSync(fullPath).isDirectory()) {
    cpSync(fullPath, join(BUILD, 'static', entry), { recursive: true });
  } else {
    cpSync(fullPath, join(BUILD, 'static', entry));
  }
}

// 3.1 Minify CSS for production (ZEMA-2683)
console.log('[build] Minifying CSS...');
const cssPath = join(BUILD, 'static', 'css', 'style.css');
if (existsSync(cssPath)) {
  const rawCss = readFileSync(cssPath, 'utf-8');
  const originalSize = Buffer.byteLength(rawCss);
  const minified = minifyCss(rawCss);
  writeFileSync(cssPath, minified);
  const minifiedSize = Buffer.byteLength(minified);
  console.log(`  ✓ style.css: ${originalSize}B → ${minifiedSize}B (${Math.round((1 - minifiedSize / originalSize) * 100)}% smaller)`);
}

// 3.5 Generate card interpretation pages (ZEMA-2610)
console.log('[build] Generating card interpretation pages...');
generateCardPages(BUILD);

// 4. Generate config.js with runtime API discovery
console.log(`[build] Generating config.js (TUNNEL_URL=${TUNNEL_URL || 'none'})...`);
writeFileSync(join(BUILD, 'static', 'js', 'config.js'), generateConfigJs(TUNNEL_URL));

// 4.5 Copy api-beacon.json (runtime API discovery — ZEMA-2620)
const beaconSrc = join(ROOT, 'api-beacon.json');
if (existsSync(beaconSrc)) {
  cpSync(beaconSrc, join(BUILD, 'api-beacon.json'));
  console.log('[build] ✓ api-beacon.json copied');
} else {
  // 빌드 시점에 비컨이 없으면 임시 비컨 생성
  const fallbackBeacon = {
    apiUrl: TUNNEL_URL ? TUNNEL_URL.replace(/\/$/, '') : '',
    source: 'build-fallback',
    updatedAt: new Date().toISOString(),
  };
  writeFileSync(join(BUILD, 'api-beacon.json'), JSON.stringify(fallbackBeacon, null, 2));
  console.log('[build] ✓ api-beacon.json generated (build fallback)');
}

// 5. Process all HTML files: inject config script + rewrite paths
console.log('[build] Processing HTML files...');
const htmlFiles = findHtmlFiles(BUILD);
for (const file of htmlFiles) {
  let html = readFileSync(file, 'utf-8');
  html = rewritePaths(html);
  html = injectConfigScript(html);
  html = injectSeoTags(html);
  writeFileSync(file, html);
  console.log(`  ✓ ${relative(BUILD, file)}`);
}

// 5.5 Rewrite canonical domain in non-HTML files (sitemap.xml, robots.txt, rss.xml)
console.log('[build] Rewriting canonical domain in XML/text files...');
function rewriteDomainInNonHtmlFiles(dir) {
  for (const entry of readdirSync(dir)) {
    const fullPath = join(dir, entry);
    const stat = statSync(fullPath);
    if (stat.isDirectory()) {
      rewriteDomainInNonHtmlFiles(fullPath);
    } else if (DOMAIN_REWRITE_EXTENSIONS.has(extname(entry)) && extname(entry) !== '.html') {
      const content = readFileSync(fullPath, 'utf-8');
      const rewritten = rewriteDomain(content);
      if (rewritten !== content) {
        writeFileSync(fullPath, rewritten);
        console.log(`  ✓ ${relative(BUILD, fullPath)}`);
      }
    }
  }
}
rewriteDomainInNonHtmlFiles(BUILD);

// 5.6 Rewrite manifest.json paths for custom domain support
// Source manifest.json has /taronyang/ prefixed paths for GitHub Pages.
// When deploying to a custom domain (SITE_URL set), strip the base path prefix.
console.log('[build] Processing manifest.json...');
(function rewriteManifestPaths() {
  const manifestPath = join(BUILD, 'manifest.json');
  if (!existsSync(manifestPath)) return;
  const normalizedExpectedUrl = `https://zemyalpha.github.io${BASE_PATH}`;
  if (GH_PAGES_URL === normalizedExpectedUrl) {
    console.log('  ✓ manifest.json — GitHub Pages paths already correct');
    return;
  }

  // Custom domain: parse and rewrite only path fields (id, start_url, scope,
  // icons, shortcuts) so unrelated values like name/description are never touched.
  const manifest = JSON.parse(readFileSync(manifestPath, 'utf-8'));
  const prefix = `${BASE_PATH}/`;
  const stripBasePath = (value) =>
    typeof value === 'string' && value.startsWith(prefix)
      ? '/' + value.slice(prefix.length)
      : value;

  if (typeof manifest.id === 'string') manifest.id = stripBasePath(manifest.id);
  if (typeof manifest.start_url === 'string') manifest.start_url = stripBasePath(manifest.start_url);
  if (typeof manifest.scope === 'string') manifest.scope = stripBasePath(manifest.scope);
  if (Array.isArray(manifest.icons)) {
    manifest.icons = manifest.icons.map((icon) => ({ ...icon, src: stripBasePath(icon.src) }));
  }
  if (Array.isArray(manifest.shortcuts)) {
    manifest.shortcuts = manifest.shortcuts.map((shortcut) => ({
      ...shortcut,
      url: stripBasePath(shortcut.url),
      icons: Array.isArray(shortcut.icons)
        ? shortcut.icons.map((icon) => ({ ...icon, src: stripBasePath(icon.src) }))
        : shortcut.icons,
    }));
  }

  writeFileSync(manifestPath, JSON.stringify(manifest, null, 2) + '\n');
  console.log(`  ✓ manifest.json — stripped ${BASE_PATH}/ prefix for custom domain`);
})();

// 6. Create directory-based routing for clean URLs
console.log('[build] Creating directory-based routes...');
for (const page of ROUTED_PAGES) {
  const sourceFile = join(BUILD, `${page}.html`);
  const targetDir = join(BUILD, page);
  if (existsSync(sourceFile)) {
    mkdirSync(targetDir, { recursive: true });
    cpSync(sourceFile, join(targetDir, 'index.html'));
    console.log(`  ✓ ${page}/index.html`);
  }
}

